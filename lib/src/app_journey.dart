class AppJourney {

  /// this class is used to store the data of the journey
  /// it has three properties
  /// image, title and description
  /// the image is the image of the journey
  /// the title is the title of the journey
  /// the description is the description of the journey

  const AppJourney({
    required this.image,
    required this.title,
    required this.description,
  });

  final String image;
  final String title;
  final String description;
}
